func appendBytes(bytes ...[]byte) []byte {
	newBytes := []byte{}
	for _, b := range bytes {
		newBytes = append(newBytes, b...)
	}
	return newBytes
}

func Test_Scan(t *testing.T) {

	garbageBytes := []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0x5A, 0x0B, 0x9D}
	preamble := []byte{0xFE, 0xFE}
	postamble := []byte{0xFE, 0x0D}

	cases := []struct {
		name   string
		input  []byte
		expect [][]byte
	}{
		{
			name:   "single data bytes scan",
			input:  appendBytes(garbageBytes, preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}, postamble, garbageBytes),
			expect: append([][]byte{}, []byte{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}),
		},
		{
			name:   "double data bytes scan",
			input:  appendBytes(preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}, postamble, garbageBytes, preamble, []byte{0x02, 0xCD, 0xEF, 0x56, 0x78}, postamble),
			expect: append([][]byte{}, []byte{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}, []byte{0x02, 0xCD, 0xEF, 0x56, 0x78}),
		},
		{
			name:   "mutiple data bytes scan",
			input:  appendBytes(preamble, []byte{0x01, 0x56, 0x78}, postamble, garbageBytes, preamble, []byte{0x02, 0x01, 0x78}, postamble, garbageBytes, garbageBytes, preamble, []byte{0xCD, 0x00, 0x12, 0x34}, postamble),
			expect: [][]byte{{0x01, 0x56, 0x78}, {0x02, 0x01, 0x78}, {0xCD, 0x00, 0x12, 0x34}},
		},
		{
			name:   "start with garbage bytes",
			input:  appendBytes(garbageBytes, preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}, postamble, garbageBytes, preamble, []byte{0x02, 0xCD, 0xEF, 0x56, 0x78}, postamble),
			expect: [][]byte{{0x01, 0xCD, 0xEF, 0x12, 0x34, 0x56, 0x78}, {0x02, 0xCD, 0xEF, 0x56, 0x78}},
		},
		{
			name:   "with special value 0xFE",
			input:  appendBytes(preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0xF0, 0x78}, postamble, garbageBytes, garbageBytes, preamble, []byte{0x02, 0xFE, 0xF0, 0xFE, 0xF0}, postamble),
			expect: [][]byte{{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0x78}, {0x02, 0xFE, 0xFE}},
		},
		{
			name:   "with 0x0D cmd",
			input:  appendBytes(preamble, []byte{0x0D, 0x11, 0x11}, postamble),
			expect: [][]byte{{0x0D, 0x11, 0x11}},
		},
		{
			name:   "with spacial value 0xFE and start with garbage bytes",
			input:  appendBytes(garbageBytes, garbageBytes, preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0xF0, 0x78}, postamble, garbageBytes, garbageBytes, preamble, []byte{0x02, 0xFE, 0xF0, 0xFE, 0xF0}, postamble),
			expect: append([][]byte{}, []byte{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0x78}, []byte{0x02, 0xFE, 0xFE}),
		},
		{
			name:   "bigger bytes with special value",
			input:  appendBytes(garbageBytes, preamble, []byte{0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0xFE, 0xF0, 0x1E, 0x1F, 0x20}, postamble),
			expect: [][]byte{{0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0xFE, 0x1E, 0x1F, 0x20}},
		},
		{
			name:   "mutiple preamble",
			input:  appendBytes(garbageBytes, preamble, preamble, preamble, []byte{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0xF0, 0x78}, postamble, garbageBytes, garbageBytes, preamble, []byte{0x02, 0xFE, 0xF0, 0xFE, 0xF0}, postamble),
			expect: [][]byte{{0x01, 0xCD, 0xEF, 0x12, 0xFE, 0x78}, {0x02, 0xFE, 0xFE}},
		},
	}

	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			// When.
			mockReader := bytes.NewReader(c.input)
			scanner := bufio.NewScanner(mockReader)
			scanner.Split(scan)

			result := [][]byte{}
			for scanner.Scan() {
				result = append(result, scanner.Bytes())
			}

			// Then.
			require.ErrorIs(t, scanner.Err(), nil)
			require.Equal(t, len(c.expect), len(result))
			for i := range result {
				require.Equal(t, c.expect[i], result[i])
			}

		})
	}
}

func Test_Frame(t *testing.T) {
	preamble := []byte{0xFE, 0xFE}
	postamble := []byte{0xFE, 0x0D}

	cases := []struct {
		name   string
		input  []byte
		expect []byte
	}{
		{
			name:   "normal",
			input:  []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0x5A, 0x0B, 0x9D},
			expect: appendBytes(preamble, []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0x5A, 0x0B, 0x9D}, crc([]byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0x5A, 0x0B, 0x9D}), postamble),
		},
		{
			name:   "with special byte",
			input:  []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0x0B, 0x9D},
			expect: appendBytes(preamble, []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0xF0, 0x0B, 0x9D}, crc([]byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0x0B, 0x9D}), postamble),
		},
		{
			name:   "with mutiple special byte",
			input:  []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0xFE, 0x9D},
			expect: appendBytes(preamble, []byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0xF0, 0xFE, 0xF0, 0x9D}, crc([]byte{0xA6, 0x75, 0xF8, 0x23, 0xE9, 0x44, 0xC0, 0xFE, 0xFE, 0x9D}), postamble),
		},
	}

	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			// When.
			result := frame(c.input...)

			// Then.
			require.Equal(t, c.expect, result)
		})
	}
}

func Test_TransformCode(t *testing.T) {
	cases := []struct {
		input  string
		expect []byte
	}{
		{
			"0",
			[]byte{0x0F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF},
		},
		{
			"0000",
			[]byte{0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF},
		},
		{
			"000",
			[]byte{0x00, 0x0F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF},
		},
		{
			"123456",
			[]byte{0x12, 0x34, 0x56, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF},
		},
		{
			"98124",
			[]byte{0x98, 0x12, 0x4F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF},
		},
		{
			"12345678901234",
			[]byte{0x12, 0x34, 0x56, 0x78, 0x90, 0x12, 0x34, 0xFF},
		},
	}

	for _, c := range cases {
		t.Run(c.input, func(t *testing.T) {
			// When.
			result := transformCode(c.input)

			// Then.
			require.Equal(t, c.expect, result)
		})
	}
}
